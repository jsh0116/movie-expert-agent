            "2. 비전 보드 만들기: 목표를 시각화하여 매일 확인\n"
            "3. 작은 승리 축하하기: 작은 성취도 기록하고 축하\n"
            "4. 환경 설계: 동기부여를 유지할 수 있는 환경 조성\n"
            "5. 책임 파트너: 함께 목표를 공유할 파트너 찾기"
