            "웰니스 핵심 팁:\n"
            "1. 수면 위생: 일정한 취침/기상 시간, 블루라이트 차단\n"
            "2. 운동 루틴: 주 3-5회, 30분 이상 중강도 운동\n"
            "3. 영양 균형: 가공식품 줄이고, 채소/과일/단백질 충분히\n"
            "4. 수분 섭취: 하루 2L 이상 물 마시기\n"
